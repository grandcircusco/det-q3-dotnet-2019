    public class Challenge
    {
        //Create a method called AddTwoNumers that takes in two numbers
        //(int) into the method. Add those two numbers together and return
        //the sum.
        public static int AddTwoNumbers(int num, int num2)
        {
            return num + num2;
        }

        public static int MultiplyTwoNumbers(int num, int num2)
        {
            return num * num2;
        }
    }
